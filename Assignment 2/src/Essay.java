public class Essay extends GradedActivity{
	
	private double ptsGrammar;
	private double ptsSpelling;
	private double ptsContent;
	
	public double getPtsGrammar() {
		return ptsGrammar;
	}
	public void setPtsGrammar(double ptsGrammar) {
		this.ptsGrammar = ptsGrammar;
	}
	public double getPtsSpelling() {
		return ptsSpelling;
	}
	public void setPtsSpelling(double ptsSpelling) {
		this.ptsSpelling = ptsSpelling;
	}
	public double getPtsContent() {
		return ptsContent;
	}
	public void setPtsContent(double ptsContent) {
		this.ptsContent = ptsContent;
	}
	
}
